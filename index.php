                                            principal cellist of the iPalpiti
                                            orchestra in Los Angeles, USA where after his debut in the famous Disney
                                            hall he was referred to as a
                                            “superb cellist” by the “LA Times”. Ofer performs as guest principal cellist
                                            in many orchestras
                                            including the SWR symphony orchestra, Bergen Philharmonic orchestra in
                                            Norway, The
                                            Kammerakademie Neuss, and the Tel-Aviv Soloist Ensemble. In 2014 Ofer was
                                            one of the principal
                                            cellists of the Jenaer Philharmonie, Germany.
