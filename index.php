                                        <p>
                                            Ofer Canetti graduated his studies in The Buchmann-Mehta School of music at
                                            the Tel-Aviv University
                                            with Prof. Hillel Zori, and was granted the Bachelor of Music Degree of
                                            Cello playing followed by
                                            studies for the "Diplom" degree in the Hochschule für Musik in Köln,
                                            Germany, with Prof. Claus
                                            Kanngiesser. In June 2012, Ofer completed his Master degree studies of Cello
                                            playing in the Mozarteum
                                            University, Salzburg, Austria, under the guidance of Prof. Clemens Hagen and
                                            has continued his
                                            studies in the Post-graduate program. In addition, Ofer was studying
                                            regularly with Prof. Uzi Wiesel.
                                            Throughout his studies, Ofer attended master-classes and lessons with
                                            renowned artists such as Prof.
                                            Bernard Greenhouse, prof. Janos Starker, Prof. Wolfgang Boettcher and many
                                            others.
                                        </p>

                                        <p>
                                            Ofer performed his Debut Recital in the Carnegie-Hall, New-York in February,
                                            2010.
                                            He performed as a soloist with Stuttgart chamber orchestra, Deutsche
                                            Kammerakademie Neuss,
                                            Georgische Kammerorchester Ingolstadt, Jenaer Philharmonie, Moscow Chamber
                                            Orchestra of the
                                            Slobotkin Center, Haifa Symphony Orchestra, the Salzburg Chamber Soloists
                                            and others, and regularly
                                            performs in recitals around Israel and Europe.
                                            He recorded numerous times as a soloist and with different chamber groups in
                                            the Israeli classicalmusic Radio station "Kol Hamusika", the SWR Stuttgart
                                            and the ORF in Austria.
                                        </p>

                                        <p>
                                            Ofer Canetti is a Prize winner of the “Bucchi” International Competition for
                                            Cello playing in the 20th21st centuries, Rome, 2011, and the "Enerico
                                            Mainardi Competition", Salzburg 2012. He won the
                                            Buchman-Mehta school of music competitions in the Solo instrument and
                                            Chamber music categories
                                            as well as special prizes in the "Allegro Vivo" festival in Austria, and the
                                            "Paul Ben-Haim" competition
                                            in Israel. He received with excellence of the American-Israeli cultural
                                            foundation scholarship and from
                                            2007 was granted the prestigious scholarship for studies abroad.
                                        </p>

                                        <p>
                                            From 2017 until 2019 Ofer was a member of the highly acclaimed “Amar
                                            Quartet” in Switzerland with
                                            them he regularly performs in many festivals and venues around Europe. Among
                                            his chamber music
                                            activities, Ofer is regularly invited to perform with appreciated players
                                            such as Ida Bieler, Julia
                                            Schröder, Thomas Reif, Jan Bjøranger, Howard Klug, Vladimir Ivanov, Daniel
                                            Ottensamer, James
                                            Maddox and Claudio Bohórquez. In 2015 Ofer has joined the Aviv Quartet as a
                                            season guest cellist
                                            where he performed in the Wigmore hall, London, the Gaia Festival,
                                            Switzerland and concert tours in
                                            Israel, England, Italy, Holland and Belgium. In 2011, Ofer was one of the
                                            founding members of the
                                            "Testore Quartet" with them he had performed in many concert venues and
                                            festivals around Europe.
                                        </p>

                                        <p>
                                            Since 2019 Ofer is a solocellist of the Stuttgart Chamber Orchestra.
                                            2011-2018 he was a principal cellist
